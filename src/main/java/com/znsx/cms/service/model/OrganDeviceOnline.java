public class OrganDeviceOnline {
	private String organName;
	private String deviceNumber;
	private String onlineDeviceNumber;
	private String onlineRate;

	public String getOrganName() {
		return organName;
	}

	public void setOrganName(String organName) {
		this.organName = organName;
	}

	public String getDeviceNumber() {
		return deviceNumber;
	}

	public void setDeviceNumber(String deviceNumber) {
		this.deviceNumber = deviceNumber;
	}

	public String getOnlineDeviceNumber() {
		return onlineDeviceNumber;
	}

	public void setOnlineDeviceNumber(String onlineDeviceNumber) {
		this.onlineDeviceNumber = onlineDeviceNumber;
	}

	public String getOnlineRate() {
		return onlineRate;
	}

	public void setOnlineRate(String onlineRate) {
		this.onlineRate = onlineRate;
	}

}
